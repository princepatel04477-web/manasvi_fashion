-- 1. Automated Supabase Auth Synchronization Trigger & Function
-- When a real user registers on your website using Supabase Auth (auth.users),
-- this database function runs AUTOMATICALLY inside Supabase to extract and sync 
-- their email, name, phone, address, and city directly into the public.users profile!

CREATE OR REPLACE FUNCTION public.handle_new_user_sync()
RETURNS TRIGGER AS $$
BEGIN
    INSERT INTO public.users (
        id, 
        name, 
        email, 
        password_hash, 
        role, 
        phone, 
        shipping_address, 
        city, 
        postal_code
    )
    VALUES (
        NEW.id::text,
        COALESCE(NEW.raw_user_meta_data->>'name', NEW.raw_user_meta_data->>'full_name', 'Boutique Customer'),
        NEW.email,
        '', -- Password hash is managed internally by Supabase Auth (auth.users.encrypted_password)
        'customer',
        NEW.phone,
        COALESCE(NEW.raw_user_meta_data->>'shipping_address', NEW.raw_user_meta_data->>'address', ''),
        COALESCE(NEW.raw_user_meta_data->>'city', ''),
        COALESCE(NEW.raw_user_meta_data->>'postal_code', NEW.raw_user_meta_data->>'pincode', '')
    );
    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Trigger to execute the function AFTER a new signup record is added in auth.users
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW EXECUTE FUNCTION public.handle_new_user_sync();


