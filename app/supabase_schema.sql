-- ============================================================
-- 0. Products Table (Public catalogue — no auth required to read)
-- ============================================================
CREATE TABLE IF NOT EXISTS public.products (
    id TEXT PRIMARY KEY DEFAULT 'p-' || extract(epoch from now())::text,
    slug TEXT UNIQUE NOT NULL,
    title TEXT NOT NULL,
    category TEXT NOT NULL,                           -- 'kurtis' | 'dresses'
    product_type TEXT NOT NULL,                       -- 'kurti' | 'tunic_top' | 'dress'
    subcategory TEXT,
    description TEXT NOT NULL DEFAULT '',
    fabric TEXT,
    sleeve_type TEXT,
    color TEXT,
    price NUMERIC(10, 2) NOT NULL DEFAULT 0,
    compare_at_price NUMERIC(10, 2),
    sizes JSONB NOT NULL DEFAULT '[]',                -- string[] e.g. ["S","M","L"]
    images JSONB NOT NULL DEFAULT '[]',               -- string[] of image paths
    stock INTEGER NOT NULL DEFAULT 0,
    rating NUMERIC(3, 1) DEFAULT 5.0,
    reviews INTEGER DEFAULT 0,
    is_new BOOLEAN DEFAULT false,
    color_variants JSONB DEFAULT '[]',                -- [{name, hex, image}]
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc', now()) NOT NULL
);

-- Enable RLS
ALTER TABLE public.products ENABLE ROW LEVEL SECURITY;

-- Allow ANYONE (anon + authenticated) to read products — public catalogue
CREATE POLICY "Public can read products" ON public.products
    FOR SELECT TO anon, authenticated
    USING (true);

-- Only authenticated admins can insert/update/delete products
CREATE POLICY "Admins can manage products" ON public.products
    FOR ALL TO authenticated
    USING (true)
    WITH CHECK (true);

-- 1. Users Table (Secure authentication & authorization profiles)
CREATE TABLE IF NOT EXISTS public.users (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    email TEXT UNIQUE NOT NULL,
    password_hash TEXT NOT NULL,
    role TEXT NOT NULL CHECK (role IN ('admin', 'seller', 'customer')) DEFAULT 'customer',
    phone TEXT,                              -- Optional phone number for checkout notifications
    shipping_address TEXT,                   -- Pre-saved physical address for express delivery
    city TEXT,                               -- Saved city
    postal_code TEXT,                        -- Saved postal pincode
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Enable RLS for Users Table
ALTER TABLE public.users ENABLE ROW LEVEL SECURITY;

-- Security Policies for Users Table
CREATE POLICY "Allow users to read their own profile" ON public.users
    FOR SELECT TO authenticated
    USING (auth.uid()::text = id OR email = auth.jwt()->>'email');

CREATE POLICY "Allow public registration inserts" ON public.users
    FOR INSERT TO anon, authenticated
    WITH CHECK (true);


-- 2. Orders Table (Wardrobe acquisitions & purchase records)
CREATE TABLE IF NOT EXISTS public.orders (
    id TEXT PRIMARY KEY,
    customer_name TEXT NOT NULL,
    customer_email TEXT NOT NULL,
    customer_phone TEXT,
    items JSONB NOT NULL, -- Detailed order line items [ { productId, title, price, qty, size, image, slug } ]
    total_amount NUMERIC(10, 2) NOT NULL,
    status TEXT NOT NULL CHECK (status IN ('pending', 'processing', 'shipped', 'delivered', 'returned', 'cancelled')) DEFAULT 'pending',
    payment_status TEXT NOT NULL CHECK (payment_status IN ('paid', 'unpaid', 'failed', 'refunded')) DEFAULT 'unpaid',
    shipping_address TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Enable RLS for Orders Table
ALTER TABLE public.orders ENABLE ROW LEVEL SECURITY;

-- Security Policies for Orders Table
CREATE POLICY "Allow users to view their own orders" ON public.orders
    FOR SELECT TO authenticated
    USING (customer_email = auth.jwt()->>'email');

CREATE POLICY "Allow secure checkout inserts" ON public.orders
    FOR INSERT TO authenticated
    WITH CHECK (customer_email = auth.jwt()->>'email');


-- 3. Contact Submissions Table (Public inquiry captures)
CREATE TABLE IF NOT EXISTS public.contact_submissions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    name TEXT NOT NULL,
    email TEXT NOT NULL,
    phone TEXT,
    inquiry_type TEXT NOT NULL CHECK (inquiry_type IN ('styling', 'order', 'collaboration', 'general')),
    message TEXT NOT NULL
);

-- Enable RLS for Contact Submissions
ALTER TABLE public.contact_submissions ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow anonymous inserts" ON public.contact_submissions
    FOR INSERT TO anon
    WITH CHECK (true);

CREATE POLICY "Allow authenticated reads" ON public.contact_submissions
    FOR SELECT TO authenticated
    USING (true);


