-- 3. High-Performance Database View to Track Customer Purchases
-- Unpacks the JSONB items list dynamically. Allows you to retrieve exactly which 
-- products a real customer has purchased with a simple, standard SQL SELECT!
-- Query example:
-- SELECT * FROM public.customer_purchase_history WHERE customer_email = 'aishwarya@example.com';

CREATE OR REPLACE VIEW public.customer_purchase_history AS
SELECT 
    o.id AS order_id,
    o.created_at AS purchase_date,
    o.customer_name,
    o.customer_email,
    o.customer_phone,
    o.status AS order_status,
    o.payment_status,
    item->>'productId' AS product_id,
    item->>'title' AS product_title,
    (item->>'price')::numeric AS unit_price,
    (item->>'qty')::int AS quantity,
    item->>'size' AS selected_size,
    item->>'slug' AS product_slug
FROM public.orders o,
LATERAL jsonb_array_elements(o.items) AS item;
